def square(x, y):
    "Draw square using path at (x, y)."
    path.up()
    path.goto(x, y)
    path.down()
    path.begin_fill()

    for count in range(4): #  Se genera el tablero
        path.forward(20)
        path.left(90)

    path.end_fill()


def offset(point):
    "Return offset of point in tiles."
    x = (floor(point.x, 20) + 200) / 20
    print("x", x)
    y = (180 - floor(point.y, 20)) / 20
    print("y", y)
    index = int(x + y * 20)
    print("index", index)
    return index


def valid(point):
    "Return True if point is valid in tiles."
    index = offset(point)

    if tiles[index] == 0:
        return False

    index = offset(point + 19)

    if tiles[index] == 0:
        return False

    return point.x % 20 == 0 or point.y % 20 == 0


def world():
    "Draw world using path."
    bgcolor('black')
    path.color('blue')

    for index in range(len(tiles)):
        tile = tiles[index]

        if tile > 0:
            x = (index % 20) * 20 - 200
            y = 180 - (index // 20) * 20
            square(x, y)

            if tile == 1:
                path.up()
                path.goto(x + 10, y + 10)
                path.dot(2, 'white')


def move():
    "Move pacman and all ghosts."
    writer.undo()
    writer.write(state['score'])

    clear()

    if valid(pacman + aim): # Si el movimiento es posible, moverse
        pacman.move(aim)

    index = offset(pacman) # indice del objeto. El indice se encuentra por una transformada del plano con respecto a la matriz.

    if tiles[index] == 1: # Si el indice esta lleno, entonces ese indice se vuelve 2 y ya no esta lleno
        tiles[index] = 2
        state['score'] += 1 # se le agrega al score
        x = (index % 20) * 20 - 200
        y = 180 - (index // 20) * 20
        square(x, y) # se pinta el square

    up()
    goto(pacman.x + 10, pacman.y + 10)
    dot(20, 'yellow')

    for point, course in ghosts:
        if valid(point + course): # Si el rumbo actual es valido, moverse
            point.move(course)
        else:
            options = [
                vector(5, 0),
                vector(-5, 0),
                vector(0, 5),
                vector(0, -5),
            ]
            plan = choice(options) # Random.choice
            course.x = plan.x # se actualiza el curso con alguna opcion aleatoria en x y y
            course.y = plan.y

        up()
        goto(point.x + 10, point.y + 10) # se mueve y dibuja
        dot(20, 'red')

    update()

    for point, course in ghosts:
        if abs(pacman - point) < 20:
            return

    ontimer(move, 100)


def change(x, y):
    "Change pacman aim if valid."
    if valid(pacman + vector(x, y)):
        aim.x = x
        aim.y = y


